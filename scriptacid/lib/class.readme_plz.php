/**
 * Описание алгоритма подколючения файлов "dload.*.php", "class.*.php" и "bload.*.php"
 * 		dload - сокращение от direct load.
 * 		bload - сокращене от before class load
 * 
 * Файлы "dload." загружаются автоматически из папки /scriptacid/lib при старте системы
 * и из папки /scriptacid/modules/<MODULE_ID>/lib при подключении модуля.
